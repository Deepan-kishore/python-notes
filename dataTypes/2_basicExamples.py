# Examples of different data types in Python
# 1. Numbers
a = 12               # integer
b = 12.56           # floating-point number
c = 3 + 5j          # complex number

# 2. Sequences:
string = "charectersSequence"          # string
listExample = [1, '23', 'anyTypeAllowed']  # list
tupleExample = (2, 'anyTypeAllowed')    # tuple
rangeExample = range(1, 10, 2)         # range from 1 to 10 with step 2

# 3. Mapping:
dictionaryExample = {'key1': 'value1', 'key2': 23}  # dictionary

# 4. Set:
setExample = {1, 2, 3}                     # set
frozenSetExample = frozenset([1, 2, 3])  # frozenset

# 6. Binary:
bytesExample = b"ConvertedToByte"               # bytes
byteArrayExample = bytearray([12, 21])     # bytearray
memoryViewExample = memoryview(b"ConvertedToByte")  # memoryview

# 7. None : keyword -> None
noneExample = None  # NoneType
